package model;

class Classmate {

    private String name;
    private String age;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getage() {
        return this.age;
    }

    public void setage(String age) {
        this.age = age;
    }
}